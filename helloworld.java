// cada linha de código deve estar dentro de uma 'class'
//por exemplo, abaixo, a nossa class se chama main
public class helloworld {
    // main() é um metodo, qualquer código que estiver dentro de main() será executado
    public static void main(String[] args) {
        //'out' é abreviado de Output, 'System' é um metodo próprio do Java e println() é outro método
        System.out.println("hello world!");
        System.out.println("olá mundo!");
        System.out.println("konnichiwa!");
    }

}
// as conchetes servem para delimitar um bloco de código